        <section class="acerca-de">
            <div class="contenedor">
                <h2 class="sobre-nosotros">Constitución de la iglesia</h2>
                
                <p class="parrafo">La iglesia Cristiana Evangélica “Sebastián Pagador” U.C.E.  empezó a organizarse a la cabeza de 
                    creyentes evangélicos emigrantes de diferentes provincias y comunidades del Departamento de Oruro. 
                    Algunos llegaron a la ciudad de Cochabamba buscando mejores condiciones de vida para su familia, 
                    otros preparados y enviados específicamente para la evangelización.</p>
                <p class="parrafo">Aquellos creyentes que llegaron de diferentes zonas del occidente habitaron en diferentes viviendas 
                    colindantes a la iglesia de “SIÓN” en calidad de inquilinos. Sin embargo, el sueño de todos era tener en un futuro 
                    una casa propia, pero también el de contar con una iglesia para ellos, en “sus oraciones siempre estaba el Templo 
                    propio para realizar el ministerio” que Dios le había encomendado.</p>
                <p class="parrafo">Es así que, por la necesidad, se organizó y coordinó entre hermanos en la fe y amigos de la vecindad, 
                    una comisión que después de buscar por algún tiempo por diferentes sectores de la ciudad de Cochabamba, encontró en 
                    inmediaciones de la parte Noreste de Valle Hermoso, un terreno con una extensión de sesenta mil metros cuadrados, 
                    para 156 socios, dentro los cuales había varios creyentes socios.</p>
