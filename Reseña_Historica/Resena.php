        <section class="acerca-de">
            <div class="contenedor">
                <h2 class="sobre-nosotros"></h2>
                
                <p class="parrafo">El 9 de febrero de 1978 se realizó la distribución y acto de posesión de manera 
                    pacífica de los terrenos comprados para los socios, los creyentes de la iglesia en forma conjunta 
                    con los otros adjudicatarios recibieron su lote y después del acto citado precedente mente se 
                    reunieron en el lugar señalado para edificar el templo donde colocaron la piedra fundamental. </p>
                <p class="parrafo">A partir de esa fecha comenzaron a reunirse en su propio lote, también los 
                    adjudicatarios desde esa fecha empezaron poco a poco a construir sus viviendas de manera 
                    rudimentaria y por ende la vecindad fue poblando la zona. Con el transcurrir del tiempo la 
                    comunidad fue tomando forma porque se fueron construyendo más casas, donde más personas 
                    fueron habitando entre las que figuraban muchos creyentes en la fe era la familia Cruz y Calle. </p>
                <p class="parrafo">Producto de una conversación entre el Hrm. Luís Cruz y Fidel Calle, se decidió 
                    oficializar los cultos regulares de la semana. Es así que un 17 de junio de 1979, se indicó de 
                    manera formal la congregación de “Sebastián Pagador” U.C.E. con los cultos de acuerdo a este orden:<br>

                    *Domingo por la mañana: Escuela dominical<br>
                    *Domingo por la noche: Culto de Evangelización<br>
                    *Martes por la noche: Culto de oración<br>
                    *Jueves por la noche: Cultos de estudio bíblico
                    </p>
