            <div class="contenedor">
                <h2 class="sobre-nosotros">Primer culto</h2>
            
                <p class="parrafo">Al llegar al terreno que Dios les había provisto a orillas del canal de riego, el nueve de octubre de mil 
                    novecientos noventa y siete se dio lugar al primer culto con un grupo de doce creyentes que se reunieron para tener 
                    dicho culto, que duró aproximadamente quince minutos. </p>
                <p class="parrafo">Este culto fue dirigido por el Hno. Luis Cruz, la meditación de la palabra de Dios estaba a cargo del pastor 
                    Guillermo Medrano, pese a no estar organizados como congregación mucho menos como iglesia, tenían la firme decisión de contar 
                    con su propio terreno para edificar una infraestructura; para ese fin aportaron como cualquier otro adjudicatario más, 
                    cumpliendo con las obligaciones establecidas por la asamblea de socios. </p>
                <p class="parrafo">Muchos creyentes tenían la obligación de asistir a las reuniones domingo tras domingo, por lo que se aprovechaba 
                    dichas reuniones para poder realizar cultos pequeños muy temprano por la mañana antes de la reunión de socios. </p>
                <p class="parrafo">Estos cultos fueron bien recibidos por todos los adjudicatarios y los vecinos ya asentados en la zona y 
                    les animaron a continuar con ellos ya que otros creyentes y simpatizantes se sumaban paulatinamente. </p>
                <p class="parrafo">Nómina de los creyentes que se reunieron por primera vez a orillas del canal de riego<br>
