                <h2 class="sobre-nosotros">Historia de la Iglesia Evangélica Sebastián Pagador “ U.C.E. ”</h2>
                
                <p class="parrafo">Para rescatar la historia de la iglesia se acudió a la primera revista ilustrada, 
                    elaborada por el cuerpo oficial del año dos mil cuatro, en ese entonces Demetrio Mamani, 
                    Valentín Gutiérrez y Benedicto Ibarra quienes son mencionados en el documento.</p>

                <p class="parrafo">“Nuestro sano propósito es exponer la información histórica concerniente a la Iglesia 
                    en colaboración con el cuerpo oficial de cada gestión, los vecinos del distrito de Sebastián 
                    Pagador también forman parte  de la historia de la iglesia, donde va relatada gran parte de 
                    la historia del barrio que empezó como una pequeña comunidad con algunas personas y hoy es 
                    un distrito  con aproximadamente 50.000 habitantes y en general a todos los interesados en 
                    tener este documento que posiblemente sea el primero que nos ilustre con datos reales y verídicos, 
                    porque los que narramos hemos vivido paso a paso estos veinticinco años y los expresamos con mucha
                    modestia y sinceridad”, expresan la Comisión Reseña Histórica. <p>

                <p class="parrafo">Se comenzó con el tesonero trabajo de personas con escasos recursos económicos y sin preparación 
                    altamente académica o teológica.</p>

                    <p class="parrafo">Por el testimonio de los mismos fundadores evidencian que “esta obra es de Dios” porque ellos no 
                    sabían lo que iban hacer, solo siguieron los pasos y guías del creador que los llevó por caminos de gran bendición y 
                    prosperidad, a pesar de sus limitaciones de carácter académico-teológico y demás dificultades. 

                    <p class="parrafo">Muchos de los cofundadores de la iglesia fueron bendecidos grandemente en sus hogares, trabajos, 
                        negocios y otras actividades. Actualmente la iglesia cuenta con miembros preparados en diferentes oficios, 
                        profesiones y estudios teológicos.</p>
           

